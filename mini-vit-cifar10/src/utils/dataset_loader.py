"""
dataset_loader.py
=================
Chargement de CIFAR-10 avec normalisation et augmentation.

CIFAR-10 : 60 000 images RGB 32x32, 10 classes.
Normalisation : soustraction de la moyenne + division par l'écart-type canal par canal.
    mean = (0.4914, 0.4822, 0.4465), std = (0.2470, 0.2435, 0.2616)
    → entrées ≈ N(0,1), compatible avec Xavier init.
"""

from torch.utils.data import DataLoader
from torchvision import datasets, transforms

CIFAR10_MEAN = (0.4914, 0.4822, 0.4465)
CIFAR10_STD  = (0.2470, 0.2435, 0.2616)

CIFAR10_CLASSES = [
    "airplane", "automobile", "bird", "cat", "deer",
    "dog", "frog", "horse", "ship", "truck"
]


def get_cifar10_loaders(data_dir="./data", batch_size=128, num_workers=2):
    """
    Retourne train_loader et val_loader pour CIFAR-10.

    Augmentation train : RandomCrop + RandomHorizontalFlip
        → régularisation implicite, meilleure généralisation.
    Validation : ToTensor + Normalize seulement (évaluation déterministe).
    """
    train_transform = transforms.Compose([
        transforms.RandomCrop(32, padding=4),
        transforms.RandomHorizontalFlip(),
        transforms.ToTensor(),
        transforms.Normalize(CIFAR10_MEAN, CIFAR10_STD),
    ])
    val_transform = transforms.Compose([
        transforms.ToTensor(),
        transforms.Normalize(CIFAR10_MEAN, CIFAR10_STD),
    ])

    train_ds = datasets.CIFAR10(data_dir, train=True,  download=True, transform=train_transform)
    val_ds   = datasets.CIFAR10(data_dir, train=False, download=True, transform=val_transform)

    train_loader = DataLoader(train_ds, batch_size=batch_size, shuffle=True,
                              num_workers=num_workers, pin_memory=True)
    val_loader   = DataLoader(val_ds,   batch_size=batch_size, shuffle=False,
                              num_workers=num_workers, pin_memory=True)
    return train_loader, val_loader

